     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user = User::find($id);
        $user->update([
            'name' => $request->name,
            'email' => $request->email,
        ]);

        $user->profile()->update([
            'profile_pic' => $request->profile_pic,
            'bio' => $request->bio,
            'address' => $request->address,
        ]);

        return redirect('profile/' . $id . '/edit');

    }

